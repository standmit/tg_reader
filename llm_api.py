#!/usr/bin/env python

from __future__ import annotations
from dataclasses import dataclass
from typing import ClassVar
import requests


@dataclass
class OllamaSession:

    SYSTEM_PROMPT: ClassVar[str] = """
        Определи, какие темы обсуждались в переданном фрагменте технического чата.

        Для каждой темы кратко укажи:

        * о чём шла речь;
        * кто задал вопрос и получил ли он ответ;
        * какой ответ или вывод был получен;
        * какой информацией поделились участники;
        * что было анонсировано.

        Объединяй связанные сообщения в одну тему, даже если они идут не подряд. Не пересказывай чат по сообщениям и не включай приветствия, шутки и малозначительные реплики.

        Не добавляй информацию от себя. Если ответа в переданном фрагменте нет, так и укажи.

        Формат:

        ### Темы обсуждения

        1. **Название темы** — краткое описание обсуждения и его результата.
        2. **Название темы** — краткое описание обсуждения и его результата.

        ### Анонсы

        * Краткое описание анонса.

        Не добавляй раздел «Анонсы», если анонсов не было.
    """

    model: str
    api_url: str
    timeout: int = 300

    def get_summary(self, chat: str) -> str | None:
        payload = {
            "model": self.model,
            "messages": [
                {
                    "role": "system",
                    "content": OllamaSession.SYSTEM_PROMPT
                },
                {
                    "role": "user",
                    "content": (
                        "Проанализируй следующий фрагмент чата:\n\n"
                        "<chat>\n"
                        f"{chat}\n"
                        "</chat>"
                    )
                }
            ],
            "stream": False,
            "think": False,

            "options": {
                "temperature": 0.2,
            }
        }

        try:
            response = requests.post(
                url=self.api_url,
                json=payload,
                timeout=self.timeout
            )
            response.raise_for_status()
        except requests.RequestException as error:
            response_text = getattr(error.response, "text", "")
            raise RuntimeError(
                f"Ошибка запроса к Ollama: {error}\n{response_text}"
            ) from error

        result = ""
        try:
            result = response.json()["message"]["content"]
        except (ValueError, KeyError, TypeError):
            raise RuntimeError(
                f"Неожиданный формат ответа от Ollama: {response.text}"
            )
        return result


